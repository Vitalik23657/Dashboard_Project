function t(key) {
    return UI_TRANSLATIONS[currentLang][key] || UI_TRANSLATIONS['en'][key] || key;
}

function translateDamageAgent(agent) {
    if (currentLang === 'en') return agent;
    if (DAMAGE_AGENT_TRANSLATIONS[agent]) return DAMAGE_AGENT_TRANSLATIONS[agent];
    for (const [en, es] of Object.entries(DAMAGE_AGENT_TRANSLATIONS)) {
        if (agent.toLowerCase().includes(en.toLowerCase())) return es;
    }
    return agent;
}

function translateStatusCell(val) {
    if (currentLang === 'en') return val;
    return COMPOSITION_TRANSLATIONS[val]
        || STRUCTURE_TRANSLATIONS[val]
        || REGENERATION_TRANSLATIONS[val]
        || DEVELOPMENT_STAGE_TRANSLATIONS[val]
        || ORIGIN_TRANSLATIONS[val]
        || val;
}

function getQualityDescription(classKey) {
    return currentLang === 'es'
        ? (QUALITY_DESCRIPTIONS_ES[classKey] || qualityDescriptions[classKey])
        : qualityDescriptions[classKey];
}